public class NedenJava {
    /**
     * Java'nın temel özellikleri nelerdir?

    Nesne yönelimli programlama (OOP) desteği
    Platform bağımsızlık (Write Once, Run Anywhere)
    Bellek yönetimi (garbage collection)
    Exception handling (istisna yönetimi)
    İleri düzey çoklu iş parçacığı (concurrency) desteği
    Java'nın farklı veri tipleri nelerdir?
    Temel veri tipleri: int, double, boolean, char, vb.
    Referans veri tipleri: String, Array, Class, vb.

     *Java'da nesne oluşturmanın farklı yolları nelerdir?

     new anahtar kelimesiyle bir sınıfın kurucusunu çağırmak
    Nesne kopyalama (clone) işlemiyle bir nesnenin bir kopyasını oluşturmak
    Deserialization ile bir nesnenin serileştirilmiş bir versiyonundan geri dönüş yapmak
    İnterface ve abstract sınıf arasındaki fark nedir?
    İnterface: Yalnızca soyut metotlar ve sabitler içerir, çoklu miras (multiple inheritance) destekler.
    Abstract sınıf: Hem soyut hem de somut metotları içerebilir, bir sınıftan türetilir.

     *Java'da static anahtar kelimesi ne anlama gelir?
    static anahtar kelimesi, bir sınıf düzeyindeki değişkenlere veya metotlara atanır. Bunlar, nesne örneği oluşturmadan doğrudan sınıf adıyla erişilebilir.

     *Java'da exception handling (istisna yönetimi) nasıl yapılır?
     try-catch blokları kullanarak hatalı kodun yakalanması ve uygun bir şekilde işlenmesi sağlanır.

     *Java'da thread'lerle (iş parçacıklarıyla) çalışmanın yöntemleri nelerdir?
    Thread sınıfını genişletmek veya Runnable arayüzünü uygulamak suretiyle thread oluşturulabilir.

     *Java'da String ve StringBuilder arasındaki fark nedir?
    String: Değiştirilemez (immutable) bir yapıya sahiptir, her değişiklik yeni bir String nesnesi oluşturur.
    StringBuilder: Değiştirilebilir (mutable) bir yapıya sahiptir, metotlarıyla mevcut nesneyi değiştirir.

     *Java'da polimorfizm nedir ve nasıl kullanılır?
    Polimorfizm, bir nesnenin farklı şekillerde davranabilmesini ifade eder
     */
}
